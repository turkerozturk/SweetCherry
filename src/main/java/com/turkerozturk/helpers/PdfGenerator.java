package com.turkerozturk.helpers;

import org.apache.pdfbox.pdmodel.PDDocument;
import org.apache.pdfbox.pdmodel.PDPage;
import org.apache.pdfbox.pdmodel.PDPageContentStream;

import java.io.*;

public class PdfGenerator {

    private final static String UNIFONT_TTF = "unifont-14.0.04.ttf";

    public static byte[] generatePDF(String content) throws IOException {
        try (PDDocument document = new PDDocument()) {
            PDPage page = new PDPage();
            document.addPage(page);

            try (PDPageContentStream contentStream = new PDPageContentStream(document, page)) {
                contentStream.beginText();

                String sourcePath = TargetFilePathHelper.getTargetPath("fonts"+ File.separator + UNIFONT_TTF);
                contentStream.setFont(FontHelper.loadFont(sourcePath, document), 12);

                contentStream.newLineAtOffset(100, 700); // Başlangıç noktası
                contentStream.showText(content);
                contentStream.endText();
            }

            ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
            document.save(outputStream);
            return outputStream.toByteArray();
        }
    }
}
