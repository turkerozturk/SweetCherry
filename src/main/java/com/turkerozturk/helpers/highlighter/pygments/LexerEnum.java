    private final String languageClassFullPath;

    LexerEnum(String languageString, String languageClassName, String languageClassFullPath) {
        this.languageString = languageString;
        this.languageClassName = languageClassName;
        this.languageClassFullPath = languageClassFullPath;
    }

    public String getLanguageString() {
        return languageString;
    }

    public String getLanguageClassName() {
        return languageClassName;
    }

    public String getLanguageClassFullPath() {
        return languageClassFullPath;
    }

    // Parametre olarak gelen dil adina gore ilgili LexerEnum degerini bulan metod
    public static LexerEnum findByLanguageString(String language) {
        for (LexerEnum lexerEnum : LexerEnum.values()) {
            if (lexerEnum.languageString.equalsIgnoreCase(language)) {
                return lexerEnum;
            }
        }
        return null; // Dil bulunamazsa null donebilir veya istege gre bir hata isleme stratejisi uygulanabilir
    }

    // Parametre olarak gelen dil adina gore ilgili LexerEnum degerinin tam adini bulan metod
    public static String findClassNameByLanguageString(String language) {
        LexerEnum lexerEnum = findByLanguageString(language);
        return lexerEnum != null ? lexerEnum.languageClassName : null;
    }

    // Parametre olarak gelen dil adina gore ilgili LexerEnum degerinin tam yolunu bulan metod
    public static String findClassFullPathByLanguageString(String language) {
        LexerEnum lexerEnum = findByLanguageString(language);
        return lexerEnum != null ? lexerEnum.languageClassFullPath : null;
    }
